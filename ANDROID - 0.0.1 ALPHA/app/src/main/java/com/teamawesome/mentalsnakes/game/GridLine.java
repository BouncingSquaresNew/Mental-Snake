public class GridLine
{
    private int xStart;
    private int yStart;
    private int xEnd;
    private int yEnd;

    public GridLine(int x1, int y1, int x2, int y2)
    {
        this.xStart = x1;
        this.yStart = y1;
        this.xEnd = x2;
        this.yEnd = y2;
    }



}
